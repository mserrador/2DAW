//  4) Calcula el perímetro de un cuadrado. Para ello, el usuario debe introducir los datos por 
// teclado mostrando por pantalla el perímetro del mismo.

let lado = parseInt(prompt("Introduce el lado del cuadrado"));
let perimetro = lado * 4;
console.log(`El perimetro del cuadrado es ${perimetro}`);

